          <strong> Profesiolandia </strong>  es el responsable del tratamiento de los datos personales proporcionados, los cuales serán protegidos conforme a lo dispuesto por la Ley General de Protección de Datos Personales en Posesión de Sujetos Obligados, y demás normatividad que resulte aplicable.
          <br> <br>
<strong> ¿Qué datos personales se recaban y para qué finalidad? </strong><br>
Los datos personales que proporcionas a través de la página web y/o aplicación móvil, son recabados por  <strong> Profesiolandia </strong>  para  acceder a los servicios ofrecidos. <br>
Los datos recabados en línea, tienen por objeto cumplir el requisito de identidad digital; crear y autenticar una cuenta de usuario para el acceso a los servicios, y enviar información relevante como ofertas y promociones,  así como para la generación de información estadística. <br>

Los datos personales que son sometidos a tratamiento son: nombre, contraseña, correo electrónico, teléfono celular, dirección, datos profesionales como títulos, certificaciones, diplomados. <br><br>

<strong> ¿Dónde se pueden ejercer los derechos de acceso, corrección/rectificación, cancelación u oposición de datos personales (derechos ARCO)? </strong><br>

Para el ejercicio de los derechos ARCO, y revocación del consentimiento para transferencias que lo requieran se podrá presentar la solicitud correspondiente ante  <strong> Profesiolandia </strong>; por correo electrónico soporte@profesiolandia.com<br>
Tiene derecho a conocer qué datos personales tenemos de usted, para qué los utilizamos y las condiciones del uso que les damos (Acceso); solicitar la corrección de su información personal en caso de que esté desactualizada, sea inexacta o incompleta (Rectificación); que se elimine la misma de nuestros registros o bases de datos cuando considere que la misma no está siendo utilizada adecuadamente (Cancelación); así como oponerse al uso de sus datos personales para fines específicos (Oposición). Estos derechos se conocen como derechos ARCO.
<br><br>

Los requisitos para la solicitud serán:
<br>
1. El nombre del titular y su correo electronico o cualquier otro medio para recibir notificaciones.
<br>
2. Los documentos que acrediten la identidad del titular y, en su caso, la personalidad e identidad de su representante.<br>
3. Descripción clara y precisa de los datos personales respecto de los que se busca ejercer alguno de los derechos ARCO.<br>
4. Descripción del derecho ARCO que se pretende ejercer.<br>
- Si solicita el acceso a sus datos personales, deberá señalar la modalidad en la que prefiere que se compartan los datos personales.<br>
- Si solicita la rectificación o corrección de sus datos personales, tendrá que señalar con claridad las modificaciones que desea que realicen, así como aportar la documentación que dé sustento a los cambios.<br>
- Si solicita cancelación de sus datos, deberá señalar las causas que motivan la solicitud de eliminación de los datos.<br>
- Si se va oponer al uso de sus datos personales, deberá manifestar las causas que lo llevan a solicitar que se concluya el uso de su información personal, o bien, que tenga que identificar con claridad las finalidades específicas respecto de las cuales no quiere que utilicen sus datos personales.<br>
5. Cualquier otro elemento o documento que facilite la localización de los datos personales.
<br>
La respuesta a su solicitud de derechos ARCO se remitirá en los plazos establecidos en los artículos 51, 52, 53 y 54 de la LGPDPPSO, se hace de su conocimiento que podrá presentar recurso de revisión ante el Instituto Nacional de Transparencia, Acceso a la Información y Protección de Datos Personales o ante la Unidad de Transparencia en caso de estar inconforme con dicha respuesta, el cual está previsto  en el Capítulo II del Título Noveno de la LGPDPPSO (publicada en el Diario Oficial de la Federación el 26 de enero de 2017) y el Artículo 40 de los Lineamientos Generales de Protección de Datos Personales para el Sector Público (Última Reforma publicada en el Diario Oficial de la Federación el 26-01-2018).<br><br>

<strong> Transferencia de datos personales </strong><br>
La información que proporciones será utilizada únicamente por <strong>Profesiolandia</strong> en los trámites solicitados por los usuarios, asimismo, no realiza transferencias que requieran tu autorización expresa, y no lleva a cabo el tratamiento de datos personales con finalidades distintas a las aquí señaladas, por lo que, de tener lugar cualquiera de dichos supuestos, se procederá a recabar el consentimiento expreso respectivo.<br><br>

<strong> Cambios a la Politica de Privacidad </strong><br>
La presente Politica de Privacidad, así como el simplificado pueden ser  objeto de modificaciones, cambios o actualizaciones, en atención la normatividad aplicable, mecanismos y políticas que al efecto se expida. Las modificaciones, cambios o actualizaciones serán hechas de conocimiento a través de la página de internet https://www.profesiolandia.com/CPolitica_Privacidad<br><br>

<strong> Portabilidad de Datos Personales </strong><br>
<strong> Profesiolandia </strong> actualmente no cuenta con formatos estructurados y comúnmente utilizados para la portabilidad de datos personales, en términos de lo dispuesto en el artículo 6 de los Lineamientos que establecen los parámetros, modalidades y procedimientos para la portabilidad de datos personales, por lo que no es posible hacer de aplicación la portabilidad de datos personales por el momento.
